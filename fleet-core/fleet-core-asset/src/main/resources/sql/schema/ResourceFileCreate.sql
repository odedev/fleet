CREATE TABLE IF NOT EXISTS `asset_resource_file` (
    `id` VARCHAR(32) NOT NULL COMMENT '主键ID',
    `name` VARCHAR(255) NOT NULL DEFAULT '' COMMENT '文件名',
    `size` BIGINT UNSIGNED NULL DEFAULT '0' COMMENT '文件大小Byte',
    `type` VARCHAR(255) NULL DEFAULT NULL COMMENT '文件MIME类型',
    `path` VARCHAR(255) NOT NULL DEFAULT '' COMMENT '文件目录路径',
    `url` VARCHAR(255) NULL DEFAULT NULL COMMENT '文件url',
    `location` TINYINT NOT NULL DEFAULT '0' COMMENT '存储位置',
    `directory` VARCHAR(255) NULL DEFAULT NULL COMMENT '目录',
    `category` VARCHAR(255) NULL DEFAULT NULL COMMENT '分类',
    `status` VARCHAR(255) NULL DEFAULT NULL COMMENT '状态',
    `scope` VARCHAR(255) NULL DEFAULT NULL COMMENT '域',
    `ext_seg1` VARCHAR(255) NULL DEFAULT NULL COMMENT '扩展段1',
    `ext_seg2` VARCHAR(255) NULL DEFAULT NULL COMMENT '扩展段2',
    `ext_seg3` VARCHAR(255) NULL DEFAULT NULL COMMENT '扩展段3',
    `ext_seg4` VARCHAR(255) NULL DEFAULT NULL COMMENT '扩展段4',
    `ext_seg5` VARCHAR(255) NULL DEFAULT NULL COMMENT '扩展段5',
    `ext_seg6` VARCHAR(255) NULL DEFAULT NULL COMMENT '扩展段6',
    `ext_seg7` VARCHAR(255) NULL DEFAULT NULL COMMENT '扩展段7',
    `ext_seg8` VARCHAR(255) NULL DEFAULT NULL COMMENT '扩展段8',
    `created_by` VARCHAR(32) NULL DEFAULT NULL COMMENT '创建人ID',
    `created_at` DATETIME NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `updated_by` VARCHAR(32) NULL DEFAULT NULL COMMENT '更新人ID',
    `updated_at` DATETIME NULL DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    `updated_count` BIGINT unsigned DEFAULT '0' COMMENT '更新次数',
    `deleted_by` VARCHAR(32) NULL DEFAULT NULL COMMENT '删除人ID',
    `deleted_at` DATETIME NULL DEFAULT NULL COMMENT '删除时间',

    PRIMARY KEY (`id`)
);
